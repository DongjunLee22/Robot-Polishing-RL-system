%% 압력‑대‑접촉력 그래프
% 파라미터 ---------------------------------------------------------------
F_max = 50;      % 포화 접촉력 [N]
k      = 15;     % 증가 속도를 결정하는 지수 상수 (값이 클수록 더 빨리 포화)

% 데이터 생성 -------------------------------------------------------------
p_tick = 0:0.1:0.4;          % 눈금용 압력 (MPa) – 0.1씩
p      = linspace(0,0.4,200);% 곡선 그리기용 압력 (조밀하게)
F      = F_max*(1 - exp(-k*p));

% 그래프 ---------------------------------------------------------------
figure;
plot(p, F, 'LineWidth',2); hold on;
scatter(p_tick, F_max*(1-exp(-k*p_tick)), 60,'filled'); % 눈금 위치 값 표시

% 축 설정
xlabel('Pressure  [MPa]');
ylabel('Force.Z [N]');
xlim([0 0.4]);   xticks(0:0.1:0.4);
ylim([0 50]);    yticks(0:10:50);
grid on;
box on;

